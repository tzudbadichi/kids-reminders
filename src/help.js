// Help / guide screen - explains everything in plain Hebrew so anyone who opens
// the app (even before signing up) understands what every part does.

import { el, clear } from "./ui.js";

function section(title, html) {
  return el("div", { class: "help-section" },
    el("h3", {}, title),
    el("div", { html }),
  );
}

// renderHelp(container, { onBack }) - if onBack is given (e.g. from the login
// screen) a "back" button is shown; inside the app the bottom tabs handle leaving.
